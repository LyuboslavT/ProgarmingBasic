text = input()

total_sum = 0

for letter in text:

    if letter == 'a':
        a = 1
        total_sum += a

    elif letter == "e":
        e = 2
        total_sum += e

    elif letter == ' i':
        i = 3
        total_sum += i

    elif letter == 'o':
        o = 4
        total_sum += o

    elif letter == 'u':
        u = 5
        total_sum += u

print(total_sum)

